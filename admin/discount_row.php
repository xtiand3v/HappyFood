<?php 
	include 'includes/session.php';

	if(isset($_POST['id'])){
		$id = $_POST['id'];
		
		$conn = $pdo->open();

		$stmt = $conn->prepare("SELECT *, discounts.discount_id AS prodid, discounts.sale_name AS prodname FROM discounts LEFT JOIN products ON products.id=discounts.product_id WHERE discounts.discount_id=:id");
		$stmt->execute(['id'=>$id]);
		$row = $stmt->fetch();
		
		$pdo->close();

		echo json_encode($row);
	}
?>